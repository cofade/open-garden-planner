def test_meeus_example_25a_declination():
    """Meeus, *Astronomical Algorithms*, 2nd ed., Example 25.a (p. 165).

    Sun position for 1992 October 13.0 TD; the book prints the apparent
    declination as delta = -7.78507 deg (-7deg 47' 06").  TD (Terrestrial
    Dynamical Time) leads UTC by ~58 s in 1992 — the sun's declination
    changes ~0.9 deg/day in October, so the TD/UTC gap contributes < 0.001
    deg, far inside the tolerance.  This row is the published-literature
    anchor: it validates the implementation against the book's own printed
    result rather than against our oracle script.
    """
    pos = solar_position(0.0, 0.0, datetime(1992, 10, 13, 0, 0, tzinfo=UTC))
    assert pos.declination_deg == pytest.approx(-7.78507, abs=0.01)


