"""Pinned-number tests for the Qt-free solar position engine (US-E1, #256).

Every number here traces to one of two provenances:

1. ORACLE ROWS — generated by the campaign oracle
   ``.claude/skills/ogp-3d-sunshade-campaign/scripts/solar_reference.py``
   (regenerated 2026-07-04, ALL CHECKS PASSED). Tolerance ±0.05° because
   production copies the same algorithm verbatim — these rows pin against
   *drift*, not against the outside world.

2. EXTERNAL / PHYSICAL ROWS — independently published facts (textbook
   example, almanac extremes, physical identities), each with a provenance
   comment. Tolerance ±0.5° / ±0.5 min — the campaign accuracy claim
   (ADR-037). These are the third-party validation the oracle rows cannot
   provide.

   NOTE: live NOAA/USNO cross-check endpoints (gml.noaa.gov solcalc,
   aa.usno.navy.mil) were unreachable from the implementation sandbox
   (network policy). Anyone with browser access is welcome to append more
   rows from the NOAA Solar Calculator — the format below shows how.
"""

from __future__ import annotations

from datetime import UTC, datetime, timedelta

import pytest

from open_garden_planner.core.solar import SolarPosition, solar_position

BERLIN = (52.52, 13.405)
EQUATOR = (0.0, 0.0)


def _at(lat_lon: tuple[float, float], *args: int) -> SolarPosition:
    lat, lon = lat_lon
    return solar_position(lat, lon, datetime(*args, tzinfo=UTC))


# ---------------------------------------------------------------------------
# 1. Oracle gate table (±0.05° vs the shipped oracle — same algorithm)
# ---------------------------------------------------------------------------

# (location, datetime args, elev, elev_refr, azimuth, decl, eot_minutes)
ORACLE_ROWS = [
    (BERLIN, (2026, 6, 21, 12, 0), 59.29, 59.30, 203.74, 23.44, -1.82),
    (BERLIN, (2026, 12, 21, 12, 0), 13.08, 13.15, 193.06, -23.44, 1.92),
    (EQUATOR, (2026, 3, 20, 12, 0), 88.14, 88.14, 91.34, -0.04, -7.43),
    (EQUATOR, (2026, 6, 21, 12, 0), 66.56, 66.56, 1.05, 23.44, -1.82),
]


@pytest.mark.parametrize("loc, when, elev, elev_refr, az, decl, eot", ORACLE_ROWS)
def test_oracle_gate_table(loc, when, elev, elev_refr, az, decl, eot):
    pos = _at(loc, *when)
    assert pos.elevation_deg == pytest.approx(elev, abs=0.05)
    assert pos.elevation_refracted_deg == pytest.approx(elev_refr, abs=0.05)
    assert pos.azimuth_deg == pytest.approx(az, abs=0.05)
    assert pos.declination_deg == pytest.approx(decl, abs=0.05)
    assert pos.eot_minutes == pytest.approx(eot, abs=0.05)


