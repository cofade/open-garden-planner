def test_naive_datetime_raises():
    with pytest.raises(ValueError, match="timezone-aware"):
        solar_position(52.52, 13.405, datetime(2026, 6, 21, 12, 0))


def test_non_utc_timezone_is_normalized():
    """A CEST-aware datetime must give the identical result as its UTC
    equivalent (14:00 CEST == 12:00 UTC)."""
    from datetime import timezone as _tz

    cest = _tz(timedelta(hours=2))
    a = solar_position(*BERLIN, datetime(2026, 6, 21, 14, 0, tzinfo=cest))
    b = _at(BERLIN, 2026, 6, 21, 12, 0)
    assert a.elevation_deg == pytest.approx(b.elevation_deg, abs=1e-9)
    assert a.azimuth_deg == pytest.approx(b.azimuth_deg, abs=1e-9)


def test_zenith_no_singularity():
    """Equator equinox noon: sun within ~2 deg of the zenith. The acos
    azimuth formulation blows up here; the atan2 form must return a finite
    azimuth and a ~88 deg elevation."""
    pos = _at(EQUATOR, 2026, 3, 20, 12, 0)
    assert pos.elevation_deg > 85.0
    assert 0.0 <= pos.azimuth_deg < 360.0


def test_is_sun_up_flag():
    assert _at(BERLIN, 2026, 6, 21, 12, 0).is_sun_up
    assert not _at(BERLIN, 2026, 6, 21, 23, 30).is_sun_up


def test_dataclass_is_frozen():
    pos = _at(BERLIN, 2026, 6, 21, 12, 0)
    with pytest.raises(AttributeError):
        pos.elevation_deg = 0.0  # type: ignore[misc]
