def test_declination_extremes_match_axial_tilt():
    """Earth's axial tilt is 23.44 deg (published constant): the solstice
    declinations must hit +/-23.44 deg."""
    summer = _at(BERLIN, 2026, 6, 21, 12, 0)
    winter = _at(BERLIN, 2026, 12, 21, 12, 0)
    assert summer.declination_deg == pytest.approx(23.44, abs=0.02)
    assert winter.declination_deg == pytest.approx(-23.44, abs=0.02)


def _max_elevation_on(loc: tuple[float, float], year: int, month: int, day: int) -> float:
    """Scan a day at 1-minute steps for the maximum geometric elevation."""
    lat, lon = loc
    best = -90.0
    t = datetime(year, month, day, 0, 0, tzinfo=UTC)
    for _ in range(1440):
        best = max(best, solar_position(lat, lon, t).elevation_deg)
        t += timedelta(minutes=1)
    return best


def test_solstice_noon_elevation_identity():
    """Spherical-geometry identity: max elevation = 90 - |lat - decl|.

    Berlin summer: 90 - |52.52 - 23.44| = 60.92; winter: 90 - |52.52 + 23.44|
    = 14.04. This is textbook geometry, independent of any implementation.
    """
    assert _max_elevation_on(BERLIN, 2026, 6, 21) == pytest.approx(60.92, abs=0.05)
    assert _max_elevation_on(BERLIN, 2026, 12, 21) == pytest.approx(14.04, abs=0.05)


def test_equation_of_time_extremes():
    """Almanac facts: EoT reaches ~-14.2 min in mid-February and ~+16.4 min
    in early November (e.g. any astronomical almanac / analemma table)."""
    feb = _at(BERLIN, 2026, 2, 11, 12, 0)
    nov = _at(BERLIN, 2026, 11, 3, 12, 0)
    assert feb.eot_minutes == pytest.approx(-14.2, abs=0.5)
    assert nov.eot_minutes == pytest.approx(16.4, abs=0.5)


def test_equinox_sunrise_azimuth_due_east():
    """On the equinox the sun rises due east (azimuth ~90 deg) at any
    latitude — a published physical fact. Scan for the sunrise minute in
    Berlin on 2026-03-20 and check its azimuth."""
    lat, lon = BERLIN
    t = datetime(2026, 3, 20, 0, 0, tzinfo=UTC)
    prev_up = solar_position(lat, lon, t).is_sun_up
    sunrise_az = None
    for _ in range(1440):
        t += timedelta(minutes=1)
        pos = solar_position(lat, lon, t)
        if pos.is_sun_up and not prev_up:
            sunrise_az = pos.azimuth_deg
            break
        prev_up = pos.is_sun_up
    assert sunrise_az is not None
    assert sunrise_az == pytest.approx(90.0, abs=0.5)


def test_northern_solar_noon_azimuth_south():
    """Northern mid-latitudes: at the hour-angle zero crossing the sun is
    due south (azimuth 180) — the classic quadrant-bug detector."""
    lat, lon = BERLIN
    best = min(
        (
            solar_position(lat, lon, datetime(2026, 6, 21, 12, 0, tzinfo=UTC) - timedelta(minutes=m))
            for m in range(-120, 120)
        ),
        key=lambda p: abs(p.hour_angle_deg),
    )
    assert best.azimuth_deg == pytest.approx(180.0, abs=0.5)


